import FolderTree from './FolderTree'
import ElUpload from '@nascent/nui/lib/upload'
import { getErrorMsg } from '@/utils/toast'
import SelectMarket from '../../components/selectMarket'
import SelectGoods from '../../components/selectGoods'
import GuideInfo from './GuideInfo'
import TagArea from '@/components/NewUi/TagArea'
import draggable from 'vuedraggable'
export default {
  name: 'imageform',
  // components: { FolderTree, SelectMarket, SelectGoods },
  components: {
    FolderTree,
    ElUpload,
    SelectMarket,
    SelectGoods,
    GuideInfo,
    TagArea,
    draggable
  },
  props: {
    labelList: {
      type: Array,
      default () {
        return []
      }
    },
    breadcrumb: {
      type: Array,
      default () {
        return []
      }
    },
    detail: {
      type: Object,
      default () {
        return {}
      }
    }
  },
  data: function () {
    return {
      loading: false,
      wechatPageTypeList: [
        { name: '商城主页面', id: 1 },
        { name: '商品', id: 2 },
        { name: '营销活动', id: 4 }
      ],
      wechatPageUrlList: [
        { codeTargetName: '首页', codeTarget: '1' },
        { codeTargetName: '分类', codeTarget: '2' },
        { codeTargetName: '我的', codeTarget: '3' }
      ],
      model: {
        isDirectory: 0,
        name: '',
        content: '',
        subdivisionIds: null,
        codeType: 1,
        marketType: null,
        codeModule: null,
        extJson: '',
        codeTarget: '',
        codeTargetName: '',
        mediaList: [],
        materialScriptType: 1
      },
      rules: {
        name: [
          {
            required: true,
            message: '请输入标题',
            trigger: ['blur', 'change']
          },
          {
            min: 0,
            max: 150,
            message: '限制长度在150个字符以内',
            trigger: ['blur', 'change']
          }
        ],
        content: [
          {
            required: true,
            message: '请输入推广文案',
            trigger: ['blur', 'change']
          },
          {
            min: 0,
            max: 1500,
            message: '限制长度在1500个字符以内',
            trigger: ['blur', 'change']
          }
        ],
        mediaList: [
          { required: true, message: '请添加素材图片', trigger: 'change' }
        ]
      },
      mType: 1,
      imageNum: 9,
      catalogue: [{ id: 0, name: '素材库' }],
      visible: false,
      defaultImgUrl:
        'https://hb3-shopguide.oss-cn-zhangjiakou.aliyuncs.com/image/material/custom-edit.png',
      showEdit: false,
      guideText: '',
      drawer: false,
      editIndex: 0,
      showEidtImg: '',
      tools: [],
      limitIndex: 0
    }
  },
  computed: {
    catalogueStr () {
      return this.catalogue.map(o => o.name).join(' > ')
    },
    mediaList: {
      get () {
        return this.model.mediaList.slice(0, this.imageNum)
      },
      set (v) {
        let arr = []
        for (const item of v) {
          if (item) {
            arr.push(item)
          }
        }
        this.model.mediaList = arr
      }
    }
  },
  watch: {
    detail (newObj) {
      const parentIds = newObj.parentPath.split('/')
      const parentNames = newObj.parentPathName.split('/')
      const tempModel = {}
      Object.keys(this.model).forEach(k => {
        tempModel[k] = !newObj[k] ? this.model[k] : newObj[k]
        // if (k === 'mediaList') {
        //   tempModel[k] = tempModel[k].filter(v =>
        //     /\.(jpg|jpeg|png|JPG|PNG|JPEG)$/.test(v)
        //   )
        // }
      })
      this.model = tempModel
      this.catalogue = parentIds.map((id, index) => ({
        id: +id,
        name: parentNames[index]
      }))
    },
    'model.codeType' (newVal) {
      this.imageNum = newVal === 2 ? 8 : 9
    }
  },
  methods: {
    getdata (data) {},
    datadragEnd (evt) {},
    removeGuideImage () {
      this.showEidtImg = ''
      let item = this.model.mediaList[this.editIndex]
      if (item) {
        item.url = ''
        this.model.mediaList[this.editIndex] = item
        this.showEidtImg = ''
      }
    },
    removeGuideImg () {
      let item = this.mediaList[this.editIndex]
      item.url = ''
      this.mediaList.splice(this.editIndex, 1)
    },
    addCustomImg () {
      // 添加坑位
      this.$refs.imageForm.clearValidate()
      this.$refs.popoverView.doClose()
      this.guideText = ''
      this.showEidtImg = ''
      this.editIndex = this.model.mediaList.length
      this.showEdit = true
    },
    handleSure () {
      //  点击拍摄指南确定
      if (this.model.mediaList.length < this.imageNum) {
        let str = this.$refs.tagArea.htmlToString(this.guideText)
        const item = this.model.mediaList[this.editIndex]
        if (item) {
          item.pitText = str
          item.url = this.showEidtImg
          this.model.mediaList[this.editIndex] = item
        } else {
          const obj = {
            pitType: 2,
            pitText: str,
            type: 1,
            url: this.showEidtImg
          }
          this.model.mediaList.push(obj)
        }
      }

      this.showEdit = false
    },
    handleCloseDia () {
      this.showEdit = false
      this.guideText = ''
      this.showEidtImg = ''
    },
    editImage (index) {
      this.editIndex = index
      let item = this.model.mediaList[index]
      this.showEidtImg = item.url
      setTimeout(() => {
        this.guideText = item.pitText
        this.guideText = this.$refs.tagArea.stringTohtml(item.pitText)
      }, 0)
      this.showEdit = !this.showEdit
    },
    handleImageType () {
      this.visible = !this.visible
    },
    toggleFolder () {
      this.$refs.folderTree.show(null, this.catalogue)
    },
    toggleLabel () {
      this.$emit('toggleLabel')
    },
    previewImage (index) {
      this.$emit('togglePreview', index, this.mediaList, 'img')
    },
    handleFolder ({ catalogue }) {
      this.catalogue = catalogue
    },
    removeImage (index) {
      this.model.mediaList.splice(index, 1)
      this.$refs.form.validateField('mediaList')
    },
    handleAvatarSuccess (res, file, fileList) {
      // this.limitIndex = this.limitIndex + 1
      // let custImgs = []
      // for (let item of this.model.mediaList) {
      //   if (item.pitType === 2) {
      //     custImgs.push(item)
      //   }
      // }
      if (this.model.mediaList.length < this.imageNum) {
        // const arr = Array.from(fileList)
        // if (this.limitIndex === arr.length) {
        //   let num = this.imageNum - custImgs.length - this.model.mediaList.length
        //   num = arr.length < num ? arr.length : num
        //   for (let index = 0; index < num; index++) {
        //     const item = arr[index]
        //     if (item.response.success) {
        //       const obj = {
        //         pitType: 1,
        //         pitText: '',
        //         type: 1,
        //         url: item.response.result.url
        //       }
        //       this.model.mediaList.push(obj)
        //     }
        //   }
        //   this.limitIndex = 0
        //   this.$refs.imageListUpload && this.$refs.imageListUpload.clearFiles()
        // }

        let obj = {
          pitType: 1,
          pitText: '',
          type: 1,
          url: res.result.url
        }
        this.model.mediaList.push(obj)
      }
      this.$refs.imageForm.clearValidate()
      this.$refs.popoverView.doClose()
    },
    handleGuideSuccess (res, file) {
      let item = this.mediaList[this.editIndex]
      if (item) {
        item.url = res.result.url
      } else {
        let obj = {
          pitType: 2,
          pitText: this.guideText,
          type: 1,
          url: res.result.url
        }
        this.mediaList[this.editIndex] = obj
      }
      this.showEidtImg = res.result.url
    },
    beforeGuideUpload (file) {
      this.beforeAvatarUpload(file)
      this.$refs.popoverView.doClose()
    },
    beforeAvatarUpload (file) {
      // 图片格式判断
      if (!/\.(jpg|jpeg|png|JPG|PNG|JPEG)$/.test(file.name)) {
        this.$notify.error('仅支持jpg/jpeg/png的图片格式')
        return false
      }
      // 图片大小判断
      if (file.size / 1024 / 1024 > 2) {
        this.$notify.warning('上传图片不得大于2MB')
        return false
      }
    },
    codeModuleChange (e) {
      this.$set(this.model, 'codeTarget', '')
      this.$set(this.model, 'codeTargetName', '')
    },
    codeTargetChange (e) {
      let codeTargetObj = e ? this.wechatPageUrlList[Number(e) - 1] : {}
      this.$set(
        this.model,
        'codeTargetName',
        codeTargetObj.codeTargetName || ''
      )
    },
    selectMarket () {
      this.$nextTick(() => {
        this.$refs.selectMarket.showToggle()
      })
    },
    selectGoods () {
      this.$nextTick(() => {
        this.$refs.selectGoods.showToggle()
      })
    },
    selectMarketBack (obj) {
      if (obj.activityId) {
        this.$set(this.model, 'codeTarget', obj.activityId)
        this.$set(this.model, 'codeTargetName', obj.activityName)
        this.$set(this.model, 'marketType', obj.marketType)
      } else if (obj.sysItemId) {
        this.$set(this.model, 'codeTarget', obj.sysItemId)
        this.$set(this.model, 'codeTargetName', obj.title)
        this.$set(
          this.model,
          'extJson',
          JSON.stringify({ mallId: obj.mallId, bankId: obj.bankId })
        )
      }
    },
    onBack (isSave) {
      this.$emit('back', isSave ? this.catalogue : null)
    },
    onSave () {
      this.$refs.form.validate(valid => {
        if (valid) {
          this.doSave()
        }
      })
    },
    doSave () {
      const params = { ...this.detail, ...this.model, mType: this.mType }
      // 控制图片数量
      params.mediaList = this.mediaList
      // 带码状态
      if (params.codeTarget === '') {
        params.codeType = 0
      }
      params.materialScriptType = 1
      for (let i = 0; i < params.mediaList.length; i++) {
        let item = params.mediaList[i]
        if (item.pitType === 2) {
          params.materialScriptType = 2
          break
        }
      }
      params.parentId = this.catalogue[this.catalogue.length - 1].id

      this.loading = true
      // 校验推广内容是否是纯空格 或换行
      let tempContent = this.model.content
      if (tempContent.replace(/\s+|[\r\n]/g, '').length === 0) {
        this.$notify.error('保存失败，推广文案不能输入纯空格或换行')
        this.loading = false
        return
      }
      this.$http
        .fetch(this.$api.guide.materialEdit, params)
        .then(resp => {
          this.$notify.success('保存成功')
          this.onBack(true)
        })
        .catch(resp => {
          this.$notify.error(getErrorMsg('保存失败', resp))
        })
        .finally(() => {
          this.loading = false
        })
    }
  },
  mounted () {
    this.catalogue =
      this.breadcrumb && this.breadcrumb.length
        ? this.breadcrumb
        : [{ id: 0, name: '素材库' }]
  }
}
