<script>
import PageTable from '@/components/NewUi/PageTable'
import NsEcharts from '@nascent/ecrp-ecrm/src/components/NsEcharts'
import ColorfulDisplay from '@/pages/Guide/CustomerGroup/components/ColorfulDisplay'
import moment from 'moment'
export default {
  name: 'GroupData',
  components: { PageTable, NsEcharts, ColorfulDisplay },
  data () {
    return {
      checkId: 1,
      endTime: '',
      startTime: '',
      dataList: [
        {
          key: 'chat_totals',
          nick: '今日总群数',
          value: 0
        },
        {
          key: 'member_totals',
          nick: '群管理好友数',
          value: 0
        },
        {
          key: 'new_member_cnts',
          nick: '今日群新增好友数',
          value: 0
        },
        {
          key: 'member_loss_cnts',
          nick: '今日群流失好友数',
          value: 0
        }
      ],
      option: {
        tooltip: {
          trigger: 'axis'
        },
        legend: {
          data: [
            '今日总群数',
            '群管理好友数',
            '今日群新增好友数',
            '今日群流失好友数'
          ],
          left: '0',
          bottom: '9%',
          icon: 'roundRect',
          itemWidth: 10,
          itemHeight: 10
        },
        grid: {
          left: 16,
          right: 16,
          bottom: 0,
          containLabel: true,
          top: '40',
          height: 291
        },
        xAxis: {
          type: 'category',
          boundaryGap: false,
          data: [],
          axisLine: {
            show: false
          },
          axisTick: {
            show: false
          },
          axisLabel: {
            color: 'rgba(0,0,0,0.25)',
            lineHeight: 24
          },
          splitLine: {
            lineStyle: {
              color: 'rgba(0,0,0,0.25)'
            }
          }
        },
        color: ['#4287FF', '#F7B586', '#95DA73', '#7962EC'],
        yAxis: {
          name: '数量',
          nameTextStyle: {
            color: 'rgba(0,0,0,0.25)',
            padding: [4, 4, 4, 24],
            verticalAlign: 'bottom',
            lineHeight: 24
          },
          type: 'value',
          axisLine: {
            show: false
          },
          axisTick: {
            show: false
          },
          axisLabel: {
            color: 'rgba(0,0,0,0.25)',
            inside: true,
            margin: 0,
            verticalAlign: 'bottom',
            lineHeight: 24
          },
          splitLine: {
            lineStyle: {
              color: 'rgba(0,0,0,0.25)'
            }
          }
        },
        series: this.setDefaultChartData()
      },
      datePickerValue: [],
      activeName: 'first',
      options: [],
      paginationToPerson: {
        size: 10,
        sizeOpts: [10],
        page: 1,
        total: 0
      },
      paginationToDate: {
        size: 10,
        sizeOpts: [10],
        page: 1,
        total: 0
      },
      selectToday: true,
      listDate: [],
      listPerson: [],
      chatRoomOwner: [],
      chatOwnerName: '',
      actionValue: '',
      ownerFlag: false,
      flag: false,
      today: '',
      last7: '',
      lart30: '',
      datePickerArr: [],
      showTodaySelect: true
    }
  },
  computed: {
    xAxisDate () { // 根据时间区间计算出每一日期
      let dateList = []
      const startTime = this.getDate(this.startTime)
      const endTime = this.getDate(this.endTime)
      while ((endTime.getTime() - startTime.getTime()) >= 0) {
        const year = startTime.getFullYear()
        const month = startTime.getMonth() + 1 < 10 ? '0' + (startTime.getMonth() + 1) : startTime.getMonth() + 1
        const day = startTime.getDate().toString().length === 1 ? '0' + startTime.getDate() : startTime.getDate()
        dateList.push(year + '-' + month + '-' + day)
        startTime.setDate(startTime.getDate() + 1)
      }
      return dateList
    }
  },
  mounted () {
    this.dealTime()
    this.dealInitTime()
    this.loadTopData()
    this.loadDateList()
    this.loadChatList()
    this.queryChatroomLeaderOptions()
    this.queryWeWorkRoomsNameOptions()
  },
  methods: {
    dealInitTime () {
      this.datePickerValue = [this.last7, this.today]
    },
    initPageData () {
      this.paginationToDate = {
        ...this.paginationToDate,
        size: 10,
        page: 1
      }
      this.paginationToPerson = {
        ...this.paginationToPerson,
        size: 10,
        page: 1
      }
    },
    /**
   * 格式化日期格式
   * @param {*} datestr
   * @return {*}
   */
    getDate (datestr) {
      const noTime = datestr.split(' ')[0]
      const temp = noTime.split('-')
      const date = new Date(temp[0], temp[1] - 1, temp[2])
      return date
    },
    outputCsvFile () {
      let that = this
      that.$notify.info('导出中，请稍后片刻')
      this.$http
        .fetch(this.$api.weWork.weWorkRooms.chat_room_list_export, {})
        .then(resp => {
          that.$notify.success('下载完成')
        })
        .catch(resp => {
          if (!resp.size === 0) {
            that.$notify.error('导出报错，请联系管理员')
          } else {
            let url = window.URL.createObjectURL(new Blob([resp]))
            let link = document.createElement('a')
            link.style.display = 'none'
            link.href = url
            let curDate = moment().format('YYYYMMDDHHmmss')
            let fileName = '群分析' + this.startTime.replaceAll('-', '') + '-' + this.endTime.replaceAll('-', '') + '.csv'
            link.setAttribute('download', fileName)
            document.body.appendChild(link)
            link.click()
          }
        })
    },
    chatNameChange (val) {
      this.initPageData()
      this.loadChatList()
      if (this.checkId === 1) {
        this.loadDateList()
      } else {
        this.loadPersonList()
      }
    },
    owenerChange (val) {
      this.initPageData()
      this.loadChatList()
      if (this.checkId === 1) {
        this.loadDateList()
      } else {
        this.loadPersonList()
      }
    },
    datePickerChange (val) {
      this.datePickerArr = val || []
      let startTime
      let endTime
      if (this.datePickerArr.length === 0) {
        this.showTodaySelect = false
        startTime = ''
        endTime = ''
      } else {
        this.showTodaySelect = false
        startTime = this.datePickerArr[0]
        endTime = this.datePickerArr[1]
      }
      this.selectToday = false
      this.datePickerValue = [startTime, endTime]
      this.initPageData()
      if (this.checkId === 1) {
        this.loadDateList()
      } else {
        this.loadPersonList()
      }
      this.loadChatList()
    },
    selectOptionClick (val) {
      this.flag = val
    },
    selectOptionOwnerClick (val) {
      this.ownerFlag = val
    },
    selectTodayClick (val) {
      this.selectToday = val === 'seven'
      const startTime = this.selectToday ? this.last7 : this.lart30
      this.showTodaySelect = true
      this.datePickerValue = [startTime, this.today]
      this.initPageData()
      this.loadChatList()
      if (this.checkId === 1) {
        this.loadDateList()
      } else {
        this.loadPersonList()
      }
    },
    lookNoStatistical () {
      this.$router.push({
        path: '/Social/OperationData/NoStatistical'
      })
    },
    // 点击tab切换
    handleClick (tab) {
      if ((this.activeName === 'first' && this.checkId === 1) || (this.activeName === 'second' && this.checkId === 2)) {
        return false
      }
      if (tab.name === 'second') {
        this.checkId = 2
        this.loadPersonList()
      } else {
        this.checkId = 1
        this.loadDateList()
      }
    },
    showMoreData () {
      // this.$refs.timeList.closeDeawer()
      this.$refs.detaList.closeDeawer()
    },
    loadTopData () {
      this.$http.fetch(this.$api.weWork.weWorkRooms.analysis_general, {}).then(res => {
        if (res.success && res.result) {
          this.dataList = this.dataList.map(item => ({
            ...item,
            value: res.result[item.key] || 0,
            isMoney: false
          }))
        }
      })
    },
    loadDateList () {
      const parms = {
        searchMap: {
          chatRoomId: this.actionValue,
          endTime: this.endTime,
          owner: this.chatOwnerName,
          startTime: this.startTime
        },
        start: (this.paginationToDate.page - 1) * this.paginationToDate.size,
        length: this.paginationToDate.size
      }
      if (this.paginationToDate.page === 1) {
        this.listDate = []
        this.paginationToDate.total = 0
      }
      this.$http
        .fetch(this.$api.weWork.weWorkRooms.analysis_page_list_by_date, parms)
        .then(res => {
          if (res.success) {
            const json = res.result
            const arr = json.data || []
            this.listDate = arr
            this.paginationToDate.total = parseInt(res.result.recordsTotal)
          }
        })
    },
    loadPersonList () {
      const parms = {
        searchMap: {
          chatRoomId: this.actionValue,
          endTime: this.endTime,
          owner: this.chatOwnerName,
          startTime: this.startTime
        },
        start: (this.paginationToPerson.page - 1) * this.paginationToPerson.size,
        length: this.paginationToPerson.size
      }
      if (this.paginationToPerson.page === 1) {
        this.listPerson = []
        this.paginationToPerson.total = 0
      }
      this.$http
        .fetch(this.$api.weWork.weWorkRooms.analysis_page_list_by_user, parms)
        .then(res => {
          if (res.success) {
            const json = res.result
            const arr = json.data || []
            this.listPerson = arr
            this.paginationToPerson.total = parseInt(res.result.recordsTotal)
          }
        })
    },
    handleSizeChangeForDate (size) {
      this.paginationToDate = {
        ...this.paginationToDate,
        size,
        page: 1
      }
      this.loadDateList()
    },
    handleCurrentChangeForDate (page) {
      this.paginationToDate.page = page
      this.loadDateList()
    },
    handleSizeChangeForPerson (size) {
      this.paginationToPerson = {
        ...this.paginationToPerson,
        size,
        page: 1
      }
      this.loadPersonList()
    },
    handleCurrentChangeForPerson (page) {
      this.paginationToPerson.page = page
      this.loadPersonList()
    },
    setDefaultChartData () {
      return [
        {
          name: '今日总群数',
          type: 'line',
          data: []
        },
        {
          name: '群管理好友数',
          type: 'line',
          data: []
        },
        {
          name: '今日群新增好友数',
          type: 'line',
          data: []
        },
        {
          name: '今日群流失好友数',
          type: 'line',
          data: []
        }
      ]
    },
    /**
   * 格式化图表数据 没有值的天数默认0
   * @param {*} list
   * @param {*} dateList
   * @return {*}
   */
    formatChart (resList, dateList) {
      const data = this.setDefaultChartData()
      const list = [...resList].reverse()
      dateList.map(item => {
        if (list.length) {
          if (item === list[0].stat_time) {
            this.setChartData(list[0].chat_totals, list[0].member_totals, list[0].new_member_cnts, list[0].member_loss_cnts, data)
            list.shift()
          } else {
            this.setChartData(0, 0, 0, 0, data)
          }
        } else {
          this.setChartData(0, 0, 0, 0, data)
        }
      })
      return data
    },
    setChartData (item1, item2, item3, item4, list) {
      list[0].data.push(item1)
      list[1].data.push(item2)
      list[2].data.push(item3)
      list[3].data.push(item4)
    },
    // 获取图表信息
    loadChatList () {
      if (this.datePickerArr.length > 0) {
        this.startTime = this.datePickerArr[0]
        this.endTime = this.datePickerArr[1]
      } else {
        this.startTime = this.selectToday ? this.last7 : this.lart30
        this.endTime = this.today
      }
      const parms = {
        chatRoomId: this.actionValue,
        endTime: this.endTime,
        owner: this.chatOwnerName,
        startTime: this.startTime
      }
      this.$http.fetch(this.$api.weWork.weWorkRooms.analysis_list, parms).then(res => {
        if (res.success) {
          this.option.xAxis.data = this.xAxisDate
          this.option.series = this.formatChart(res.result, this.xAxisDate)
        }
      })
    },
    queryChatroomLeaderOptions () {
      this.$http
        .fetch(this.$api.weWork.weWorkRooms.queryWeWorkRoomsLeaderOptions)
        .then(resp => {
          this.chatRoomOwner = resp.result
        })
        .catch(resp => {})
    },
    queryWeWorkRoomsNameOptions () {
      this.$http
        .fetch(this.$api.weWork.weWorkRooms.queryWeWorkRoomsNameOptions)
        .then(resp => {
          this.options = resp.result
        })
        .catch(resp => {})
    },
    dealTime () {
      this.today = moment().format('YYYY-MM-DD')
      this.last7 = moment()
        .subtract('days', 6)
        .format('YYYY-MM-DD')
      this.lart30 = moment()
        .subtract('days', 29)
        .format('YYYY-MM-DD')
    }
  }
}
</script>

