                  :row-style="{ height: '48px' }"
                >
                  <el-table-column prop="user_name" label="员工">
                  </el-table-column>
                  <el-table-column prop="chat_totals" label="今日群总数">
                  </el-table-column>
                  <el-table-column prop="member_totals" label="群管理客户数">
