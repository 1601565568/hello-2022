function TreeDeepSearch (data, key, children, name) {
  if (data[key] === name) {
    delete data[children]
    return [data]
  } else {
    for (let item of (data[children] || [])) {
      let res = TreeDeepSearch(item, key, children, name)
      if (res.length) {
        delete data[children]
        return [...res, data]
      }
    }
    return []
  }
}
function search (data, key, children, name) {
  let arr = []
  for (let item of (data instanceof Array) ? data : [data]) {
    arr = TreeDeepSearch(item, key, children, name)
    if (arr.length > 0) {
      break
    }
  }
  return arr
}
